#define TIMER_T3_INPUT_PRESCALER    32                      /* Timer input prescaler value                          */
#define MAX_VAL_16_BIT              10

#define m_2_pulse                   90
#define vel_2_pulse                 m_2_pulse/50

#define Kp 0.17
