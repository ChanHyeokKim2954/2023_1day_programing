/* Interrupt Service Routine of timer T2, gets triggered by rising edge on input pin of timer T2 */

int  timeNow(void){
    uint32 currentCntVal = IfxGpt12_T3_getTimerValue(&MODULE_GPT120); /* Get timer value of timer T2 */
    uint32 finalCntVal = 0; /* Variable to calculate final timer counter value */
    finalCntVal = (uint32)(currentCntVal + (MAX_VAL_16_BIT - g_previousCntVal) + ((timer_count - 1) * MAX_VAL_16_BIT));
    g_previousCntVal = currentCntVal;    /* Set g_previousCntVal to currentCntVal for the next calculation */
    return finalCntVal;
}

void setGptPWM_Duty(){
    dutyUpTime = (fTimer * (g_PWM_DUTY_CYCLE / 100.0f)) / PWM_FREQUENCY;
    dutyDownTime = (fTimer * (1 - (g_PWM_DUTY_CYCLE / 100.0f))) / PWM_FREQUENCY;
    IfxGpt12_T2_setTimerValue(&MODULE_GPT120, dutyDownTime);
    IfxGpt12_T4_setTimerValue(&MODULE_GPT120, dutyUpTime);
}

void MOTOR_CONTROL (float64 pwm){
    speed = (pwm / 255) * 100;
    if(speed < 0){
        IfxPort_setPinState(DIR_A_PIN, IfxPort_State_low);
        IfxPort_setPinState(DIR_A_PIN1, IfxPort_State_high);
        g_PWM_DUTY_CYCLE = -speed;
    }
    else{
        IfxPort_setPinState(DIR_A_PIN, IfxPort_State_high);
        IfxPort_setPinState(DIR_A_PIN1, IfxPort_State_low);
        g_PWM_DUTY_CYCLE = speed;
    }
    setGptPWM_Duty();
}

void PID_SPEED_CONTROL(void)
{
    target_encoder += target * vel_2_pulse;
    error = target_encoder - encoderPos;
    error_s += error;
    error_d = error - error_old;

    error_s = (error_s >= 50)  ?  50 : error_s;
    error_s = (error_s <= -50) ? -50 : error_s;

    pwm_output = Kp*error + Kd*error_d + Ki*error_s;

    pwm_output = (pwm_output >= 70)  ?  70 : pwm_output;
    pwm_output = (pwm_output <= 4 && pwm_output > 0)  ?  4 : pwm_output;
    pwm_output = (pwm_output <= -70) ? -70 : pwm_output;
    pwm_output = (pwm_output >= -4 && pwm_output < 0)  ?  -4 : pwm_output;

    if (fabs(error) <= 2)
    {

        MOTOR_CONTROL(0);
        error = 0;
        target = 0;
       // encoderPos = 0;
    }
    else
    {
        MOTOR_CONTROL(pwm_output);
    }
    error_old = error;
}
